package test;

public class Star5 {

	public static void main(String[] args) {
	
		for(int i =0; i<=5; i++) {
			for(int j = 1; j<=5; j++) {
				System.out.print(j);
			}
			System.out.println();
		}

	}

}
//•Line 10 - 세로줄은 총 5줄. i가 0~4 총 5번 반복하면서 한 줄씩 내려간다.
//•Line 11 - j가 1~5까지 반복하면서 j값을 출력한다. jjjjj -> 12345

