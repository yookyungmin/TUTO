package Problem1;

public class Java100Array {

	public static void main(String[] args) {
		
	
		int[] sales_table = {3, 11, 22, 33, 44};
		
		System.out.println(sales_table[3]);//33
		System.out.println(sales_table.length); //배열의 크기
		
		System.out.println(sales_table[4]); //44
		System.out.println(sales_table[sales_table.length-1]); //44 -1빼면 마지막 
	}

}
